#!/usr/bin/env node
/**
 * Generate `python/_generated_version.py` from the npm version in
 * `package.json`. Run before `sidecar:build` / `dev` so the Python
 * sidecar's `system.ready` event and logs report the same version
 * the Electron app carries.
 *
 * Output file is git-ignored; regenerated on every build.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '..')
const pkgPath = resolve(repoRoot, 'package.json')
const outPath = resolve(repoRoot, 'python', '_generated_version.py')

const { version } = JSON.parse(readFileSync(pkgPath, 'utf8'))
if (typeof version !== 'string' || version.length === 0) {
  throw new Error(`package.json version is not a string: ${JSON.stringify(version)}`)
}

const banner = '# AUTO-GENERATED by scripts/write-python-version.mjs — do not edit by hand.\n'
const body = `VERSION = ${JSON.stringify(version)}\n`

mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, banner + body, 'utf8')
console.log(`[write-python-version] wrote ${outPath} with VERSION = ${version}`)
